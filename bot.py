async def button_handler(update: Update, context: ContextTypes.DEFAULT_TYPE):
    """–û–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ—Ç –Ω–∞–∂–∞—Ç–∏—è –Ω–∞ –∫–Ω–æ–ø–∫–∏"""
    query = update.callback_query
    data = query.data
    
    if data == "start_test":
        await start_test(update, context)
    elif data == "begin_archetype":
        await begin_archetype(update, context)
    elif data.startswith("arch_"):
        await handle_archetype_answer(update, context)
    elif data == "continue_strategy":
        await continue_strategy(update, context)
    elif data == "show_archetype":
        await show_archetype(update, context)
    elif data == "start_problem":
        await start_problem(update, context)
    elif data == "begin_problem":
        await begin_problem(update, context)
    elif data.startswith("prob_"):
        await handle_problem_answer(update, context)
    elif data == "get_tale":
        await send_tale(update, context)

