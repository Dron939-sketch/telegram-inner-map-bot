async def send_problem_question(query, context):
    """–û—Ç–ø—Ä–∞–≤–ª—è–µ—Ç –≤–æ–ø—Ä–æ—Å –¥–ª—è –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–∏—è –ø—Ä–æ–±–ª–µ–º—ã"""
    archetype = context.user_data['archetype']
    question_num = context.user_data['current_question']
    questions = PROBLEM_QUESTIONS[archetype]
    
    if question_num >= len(questions):
        await calculate_problem(query, context)
        return
    
    question = questions[question_num]
    progress = get_progress_bar(question_num, len(questions))
    
    text = f"""üìä –û–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–µ –ø—Ä–æ–±–ª–µ–º—ã

{progress}

‚ùì –í–æ–ø—Ä–æ—Å {question_num + 1} –∏–∑ {len(questions)}:

{question['text']}"""
    
    keyboard = [
        [InlineKeyboardButton(option[0], callback_data=f"prob_{option[1]}")] 
        for option in question['options']
    ]
    
    reply_markup = InlineKeyboardMarkup(keyboard)
    
    await query.edit_message_text(text, reply_markup=reply_markup)

async def handle_problem_answer(update: Update, context: ContextTypes.DEFAULT_TYPE):
    """–û–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ—Ç –æ—Ç–≤–µ—Ç –Ω–∞ –≤–æ–ø—Ä–æ—Å –ø—Ä–æ–±–ª–µ–º—ã"""
