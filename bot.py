def get_progress_bar(current, total, length=10):
    """–°–æ–∑–¥–∞—ë—Ç –≤–∏–∑—É–∞–ª—å–Ω—É—é –ø–æ–ª–æ—Å—É –ø—Ä–æ–≥—Ä–µ—Å—Å–∞"""
    filled = int(length * current / total)
    bar = "‚ñà" * filled + "‚ñë" * (length - filled)
    return f"{bar} {current}/{total}"

